const fallbackUx = (signal: Signal): SignalUx => ({
  label: `${signal.type}: ${signal.action.replace(/_/g, " ")}`.toUpperCase(),
  icon: AlertTriangle,
  color: "text-muted-foreground",
  why: "Signal generated by the regime engine. Open the relevant area to see scoped detail.",
  cta: "Open",
  to:
    signal.type === "inventory"
      ? "/inventory"
      : signal.type === "production"
        ? "/operations"
        : "/procurement",
});

// Procurement actions have the highest dollar leverage — surface them first.
const typePriority: Record<string, number> = {
  procurement: 0,
  inventory: 1,
  production: 2,
};

export function PolicySignals({ signals }: PolicySignalsProps) {
  const [, setLocation] = useLocation();

  if (!Array.isArray(signals) || signals.length === 0) {
    return (
      <Card className="p-6" data-testid="card-policy-signals">
        <div className="space-y-4">
          <div className="flex items-center justify-between">
            <h3 className="font-semibold text-lg">Recommended Actions</h3>
            <Badge variant="secondary" data-testid="badge-signal-count">0</Badge>
          </div>
          <p className="text-sm text-muted-foreground">
            No regime-driven actions right now. Conditions are stable — keep standard
            procurement pace and revisit when the regime shifts.
          </p>
        </div>
      </Card>
    );
  }

  const ranked = [...signals]
    .filter((s) => s?.action && s?.description && s?.type)
    .sort((a, b) => (typePriority[a.type] ?? 9) - (typePriority[b.type] ?? 9));

  return (
    <Card className="p-6" data-testid="card-policy-signals">
      <div className="space-y-4">
        <div className="flex items-center justify-between">
          <div>
            <h3 className="font-semibold text-lg">Recommended Actions</h3>
            <p className="text-xs text-muted-foreground mt-0.5">
              Each action is tied to the current FDR regime and routes to the page
              where you can execute it.
            </p>
          </div>
          <Badge variant="secondary" data-testid="badge-signal-count">
            {ranked.length}
          </Badge>
        </div>

        <div className="space-y-3">
          {ranked.map((signal, idx) => {
            const compositeKey = `${signal.type}:${signal.action}`;
            const ux = signalConfig[compositeKey] || fallbackUx(signal);
            const Icon = ux.icon;

            return (
              <div
                key={`${compositeKey}-${idx}`}
                className="p-3 rounded-md border bg-card/50 hover-elevate"
                data-testid={`signal-${signal.action}`}
              >
                <div className="flex items-start justify-between gap-3">
                  <div className="flex items-start gap-3 flex-1 min-w-0">
                    <div className={`mt-0.5 ${ux.color}`}>
                      <Icon className="h-5 w-5" />
                    </div>
                    <div className="flex-1 space-y-1 min-w-0">
                      <div className="font-medium text-sm">{ux.label}</div>
                      <p className="text-xs text-muted-foreground leading-relaxed">
                        {signal.description}
                      </p>
                      <p className="text-xs text-muted-foreground/80 leading-relaxed pt-1">
                        <span className="font-medium text-foreground/70">
                          Why:
                        </span>{" "}
                        {ux.why}
                      </p>
                    </div>
                  </div>
                  <Badge
                    variant="outline"
                    className="text-xs whitespace-nowrap capitalize"
                  >
                    {signal.type}
                  </Badge>
                </div>
                <div className="mt-3 flex justify-end">
                  <Button
                    variant="ghost"
                    size="sm"
                    className="h-7 text-xs gap-1"
                    onClick={() => setLocation(ux.to)}
                    data-testid={`signal-action-${signal.action}`}
                  >
                    {ux.cta}
                    <ArrowRight className="h-3 w-3" />
                  </Button>
                </div>
              </div>
            );
          })}
        </div>
      </div>
    </Card>
  );
}
